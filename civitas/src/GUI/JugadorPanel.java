    Jugador jugador;
    
    void setJugador(Jugador player) {
        jugador = player;
        textoNombre.setText(jugador.getNombre());
        String sa = Float.toString(jugador.getSaldo());
        textoSaldo.setText(sa);
        String en = Boolean.toString(jugador.isEncarcelado());
        textoEncarcelado.setText(en);
        String es;
        es = Boolean.toString(jugador.getEspeculador());
        textoEspeculador.setText(es);
       
        repaint();
        
        rellenaPropiedades(jugador.getPropiedades());
    }
    
    private void rellenaPropiedades(ArrayList<TituloPropiedad> lista) {
        propiedades.removeAll();
        
        for(TituloPropiedad t : lista) {
            PropiedadPanel vistaPropiedad = new PropiedadPanel();
            vistaPropiedad.setPropiedad(t);
            propiedades.add(vistaPropiedad); 
            vistaPropiedad.setVisible(true);
        }
        
        // Se fuerza la actualizaci√≥n visual del panel propiedades y del panel del jugador
        repaint();
        revalidate();
    }
    
