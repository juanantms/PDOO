    public void setGestiones(){
        DefaultListModel gestiones = new DefaultListModel<>(); 
        ArrayList<String> opciones = new ArrayList<> (Arrays.asList("Vender", 
                "Hipotecar", "Cancelar hipoteca", "Construir casa", 
                "Construir hotel", "Terminar"));
        for(String s: opciones) {
            gestiones.addElement(s);
        }
        listaGestiones.setModel(gestiones); 
    }
     
     public void setPropiedades(Jugador jugador) {
        DefaultListModel gestiones = new DefaultListModel<>();
        ArrayList<String> n = jugador.getNombrePropiedades();
        ArrayList<String> opciones = n;
        for (String s: opciones){
            gestiones.addElement(s);
        }
        Propiedades.setModel(gestiones);
    }
     
     
     
    public void gestionar(Jugador jugador) {
        setGestiones();
        setPropiedades(jugador);
        repaint();
    }
     
